	AREA RESET, CODE, READONLY
		EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	AREA myCode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R0, #0x12345678
	MOV R1, #0
	MOV R2, #0xF
	MOV R4, #1
	MOV R5, #10
UP	CMP R0, #0
	BEQ STOP
	AND R3, R0, R2
	MUL R3, R3, R4
	MUL R4, R4, R5
	ADD R1, R3
	LSR R0, #4
	B UP
STOP 	B STOP
	AREA MYDATA, DATA, READWRITE
DST DCD 0x0
	END