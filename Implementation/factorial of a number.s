	AREA RESET, CODE, READONLY
		EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	AREA myCode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =SRC
	LDR R1, [R0]
	CMP R1, #0
	BEQ AAA
	MOV R3, #1
UP	MUL R3, R3, R1
	SUB R1, #1
	CMP R1, #0
	BHI UP
AAA	LDR R0, =DST
	STR R3, [R0]
STOP B STOP
SRC	DCD 0x0
	AREA MYDATA, DATA, READWRITE
DST DCD 0x0
	END