	AREA RESET, CODE, READONLY
		EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	AREA myCode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R1, #4
	MOV R2, #5
	MOV R3, #3
	MOV R4, #1
	MOV R5, #2
	STMDB R13!, {R1-R5}
	BL SORT
	LDMIA R13!, {R1-R5}
STOP B STOP
SORT MOV R1, #0 ;I
TOP	CMP R1, #20
	BEQ EXI
	MOV R2, #0
	MOV R3, #16
	SUB R3, R1
UP	CMP R2, R3
	BEQ NEX
	LDR R4, [R13, R2]
	ADD R2, #4
	LDR R5, [R13, R2]
	SUB R2, #4
	CMP R4, R5
	BLS MEH
	STR R5, [R13, R2]
	ADD R2, #4
	STR R4, [R13, R2]
	SUB R2, #4
MEH ADD R2, #4
	B UP
NEX ADD R1, #4
	B TOP
	
EXI BX LR
	END